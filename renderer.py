def build_dashboard_html(spec, df):
    kpis = spec.get('kpis', [])
    sections = spec.get('sections', [])
    kpi_html = ''
    for k in kpis:
        title = safe_str(k.get('title')); expr = k.get('expr','COUNT(*)'); fmt = k.get('format','auto')
        val = 0
        try:
            if expr.upper().startswith('SUM('): col = expr[4:-1]; val = df[col].sum()
            elif expr.upper().startswith('AVG('): col = expr[4:-1]; val = df[col].mean()
            elif expr.upper().startswith('COUNT('): col = expr[6:-1]; val = df[col].count()
        except:
            val = 0
        kpi_html += f"<div class='kpi-card'><div class='kpi-title'>{title}</div><div class='kpi-value'>{val}</div></div>"
    sections_html = ''
    for sec in sections:
        sec_name = safe_str(sec.get('name','Section')); viz_block = ''
        for ch in sec.get('charts', []):
            ctype = ch.get('type','bar'); x = ch.get('x'); y = ch.get('y')
            html_piece = make_plot_html(df, x or '', y or '', ctype, theme=spec.get('theme','light'))
            narrative = sec.get('narrative','') or ''
            viz_block += f"<div class='viz'><div class='viz-head'><div class='viz-title'>{safe_str(x)} vs {safe_str(y)}</div><div class='viz-type'>{safe_str(ctype)}</div></div>{html_piece}<div class='insight'>ðŸ§  {safe_str(narrative)}</div></div>"
        if viz_block:
            sections_html += f"<section><h3>{sec_name}</h3><div class='viz-grid'>{viz_block}</div></section>"
    html = TEMPLATE.render(title=safe_str(spec.get('title','Dashboard')), theme=spec.get('theme','light'), kpis=kpi_html, sections=sections_html)
    return html

def render_preview(html, height=800):
    import streamlit as st
    st.components.v1.html(html, height=height, scrolling=True)

def make_explainability_table(spec, df):
    rows = []
    for sec in spec.get('sections', []):
        for ch in sec.get('charts', []):
            x = ch.get('x'); y = ch.get('y'); ctype = ch.get('type', 'bar')
            rows.append({'section': sec.get('name'), 'x': x, 'y': y, 'type': ctype})
    return pd.DataFrame(rows)
